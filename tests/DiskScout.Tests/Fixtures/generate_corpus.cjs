// Build JSON output.
// SIZE/DATE strategy: humans annotated based on full folder context (not visible from path alone).
// We map the verdict to a plausible (size, age) so the engine's bonuses fire appropriately:
//   - Aucun/Faible (true residue): size=0, age=500d  -> bonuses fire (+20 +15 +10)
//   - Moyen / Eleve / Critique (active or recent):    size=10MB, age=30d -> no bonuses
// This reflects what would be measured on a real machine — empty old folder = residue.
function pickSizeAndDate(level, action) {
  const noBonusLevels = new Set(['ELEVE', 'CRITIQUE']);
  const recentMoyen = level === 'MOYEN' && (action === 'Garder' || action === null);
  if (noBonusLevels.has(level) || recentMoyen) {
    // Active artifact: small but non-zero, recent.
    return { sizeBytes: 10 * 1024 * 1024, lastWriteUtc: '2026-04-01T00:00:00Z' };
  }
  // True or near-residue: empty + old.
  return { sizeBytes: 0, lastWriteUtc: '2024-01-01T00:00:00Z' };
}

